struct Stack {
    var array = [String]()
    
    /// create push function
    mutating func push(_ str: String) {
        array.append(str)
    }
    
    /// create pop function
    mutating func pop() -> String? {
        guard array.count > 0 else { return nil }
        
        let lastEle = array.last
        array.removeLast()
        return lastEle
    }
}

